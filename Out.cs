using System;

public class Out
{
	public static void Println(string s)
	{
		Console.WriteLine(s);
	}

	public static void Log(string str)
	{
		Console.WriteLine(str);
	}

	public static void LogError(string str)
	{
		Console.WriteLine(str);
	}

	public static void LogWarning(string str)
	{
		Console.WriteLine(str);
	}
}